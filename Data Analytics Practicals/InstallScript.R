# Script to install the most used libraries
# use this if you need to reinstall packages
# when on a University machine using the appsanywhere version.
# you might already have the packages installed, if not, click at the top of the script
# then click the "Run" button. Each line will install a separate package

install.packages("tidyverse") # base set of packages
install.packages("odbc") # connect to data sources such as databases
install.packages("httr2") # work with http securely
install.packages("data.table") # fast data manipulation for large data set
install.packages("shiny") # working with interactive web apps
install.packages("plotly") # create interactive plots
install.packages("rmarkdown") # markdown for R
install.packages("knitr") # build rmarkdown files
install.packages("learnr") # used for the interactive tutorials
install.packages("flexdashboard") # rmarkdown to create interactive dashboards⏎